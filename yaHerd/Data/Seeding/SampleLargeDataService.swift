import SwiftData
import Foundation

/// Rebuilt large sample seed from pasture-section data in `Pasture Animals.txt`.
/// Rules used:
/// - Only pasture sections are used to create animals; the top 2026-calves list is ignored as duplicate reference.
/// - Every listed animal remains in the seed.
/// - Dead animals remain as animals with `.dead` status.
/// - Moved bulls are placed in `SE Pasture`.
/// - Other moved animals are placed in `Holding Pasture`.
/// - `UT` / `lost tag` animals stay active with an empty current tag and retired-tag history when available.
/// - Retired tags come from `/`, `think it's`, and explicit lost-tag context.
/// - No inferred names are added. Only explicit names from the notes are kept.
///
/// Generated as a best-effort normalization for the "Large" sample seed.

struct SampleLargeDataService {
    
    static func seedIfNeeded(context: ModelContext) {
        let descriptor = FetchDescriptor<Animal>()
        if let existing = try? context.fetch(descriptor), !existing.isEmpty {
            return
        }
        
        let tagColorStore = TagColorLibraryStore()
        let colorIDs = Dictionary(uniqueKeysWithValues: tagColorStore.colors.map { ($0.name, $0.id) })
        
        let pastureNames = [
            "NW Pasture",
            "SW Pasture",
            "Wally's Pasture",
            "LF Pasture",
            "NE Pasture",
            "SE Pasture",
            "Holding Pasture"
        ]
        
        var pasturesByName: [String: Pasture] = [:]
        for pastureName in pastureNames {
            let pasture = Pasture(name: pastureName)
            context.insert(pasture)
            pasturesByName[pastureName] = pasture
        }
        
        var animalsByKey: [String: Animal] = [:]
        
        for seed in animalSeeds {
            let animal = Animal(
                name: seed.name,
                tagNumber: seed.tagNumber,
                tagColorID: seed.tagColorName.flatMap { colorIDs[$0] },
                birthDate: seed.birthDate,
                status: seed.status,
                deathDate: seed.status == .dead ? seed.deathDate : nil,
                causeOfDeath: seed.status == .dead ? seed.causeOfDeath : nil,
                pasture: pasturesByName[seed.pastureName],
                sex: seed.sex,
                distinguishingFeatures: seed.features.map { DistinguishingFeature(description: $0) }
            )
            animal.location = .pasture
            context.insert(animal)
            animalsByKey[seed.key] = animal
        }
        
        for seed in animalSeeds {
            guard let animal = animalsByKey[seed.key] else { continue }
            
            if let damKey = seed.damKey, let dam = animalsByKey[damKey] {
                animal.damAnimal = dam
            }
            
            if !seed.tagNumber.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty {
                let primaryTag = animal.ensurePrimaryTagRecord()
                primaryTag.assignedAt = seed.birthDate
            }
            
            for retiredTag in seed.retiredTags {
                let tag = AnimalTag(
                    number: retiredTag.number,
                    colorID: retiredTag.colorName.flatMap { colorIDs[$0] },
                    isPrimary: false,
                    isActive: false,
                    assignedAt: seed.birthDate,
                    removedAt: seed.deathDate ?? seed.birthDate.addingTimeInterval(60 * 60 * 24 * 365),
                    animal: animal
                )
                animal.tags.append(tag)
                context.insert(tag)
            }
            
            if seed.status == .dead, let deathDate = seed.deathDate {
                let record = StatusRecord(
                    date: deathDate,
                    oldStatus: .active,
                    newStatus: .dead,
                    animal: animal
                )
                context.insert(record)
            }
            
            if seed.isPregnant {
                let check = PregnancyCheck(
                    date: pregnancyCheckDate(for: seed.pastureName),
                    result: .pregnant,
                    technician: "Sample Large seed",
                    estimatedDaysPregnant: nil,
                    dueDate: nil,
                    sireAnimal: nil,
                    workingSession: nil,
                    animal: animal
                )
                context.insert(check)
            }
            
            for movement in seed.movements {
                let record = MovementRecord(
                    date: movement.date,
                    fromPasture: movement.fromPasture,
                    toPasture: movement.toPasture,
                    animal: animal
                )
                context.insert(record)
            }
        }
        
        do {
            try context.save()
        } catch {
            assertionFailure("Failed to save SampleLargeDataService seed data: \(error.localizedDescription)")
        }
    }
    
    private static func pregnancyCheckDate(for pastureName: String) -> Date {
        switch pastureName {
        case "NW Pasture":
            return seedDate(2025, 8, 20)
        case "SW Pasture":
            return seedDate(2025, 8, 28)
        case "Wally's Pasture":
            return seedDate(2025, 9, 5)
        case "LF Pasture":
            return seedDate(2025, 9, 10)
        case "NE Pasture":
            return seedDate(2025, 9, 15)
        default:
            return seedDate(2025, 9, 1)
        }
    }
    
    private static func seedDate(_ year: Int, _ month: Int, _ day: Int) -> Date {
        Calendar.current.date(from: DateComponents(year: year, month: month, day: day)) ?? .now
    }
    
